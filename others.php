			<h3>Pickled ramen eggs</h3>
			<p>These usually accompany all bowls at any ramen joint worth its salt but they are delicious on their own. These are based on the best version of them I have had so far.</p>
			<p>A lot of people will say that to get the perfect soft boiled eggs you need to pay close attention to the time and temperature and this is true but one of the more important things is that you need to have a bowl of ice water nearby to stop them from cooking when they are done.</p>
			<p>Around the 6 minute mark is the generally accepted time for soft boiled eggs. But they have to be somewhat near room temperature when you put them in the boiling water and you have to be sensitive to how much the  eggs are changing the temperature of the water. If you have 6 or 7 cool eggs and you put them in a small pot of boiling water you are actually losing some boiling time on the eggs and you need adjust your total cook time accordingly. Just remeber that they need to be actually boiling for 5 minutes 45 seconds no matter what to even get close to being "soft boiled."</p>
			<p>With all that being said lets get to the marinade that you are going to put them in</p>
			<ul>
				<li>3/4 cup of soy sauce</li>
				<li>1/4 cup of Kotteri(thick) Mirin</li>
				<li>teaspoon of honey or sugar</li>
				<li>1/3 teaspoon of sesame oil</li>
				<li>1/3 teaspoon of chili (rooster) sauce to give it that bite</li>
			</ul>
			<p>you may need to downsize or upsize this recipe depending on how many eggs and how you are marinading them. This recipe will marinade around 6 in a large ziploc bag vacuum sealed with a straw.</p>

			<p>There are a number of ways of marinading eggs just be sure that the eggs are touching the container or air as little as possible</p>

